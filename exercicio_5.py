# Desenvolva um programa que leia 4 notas e apresente média aritmética destes números.

nota_1 = float(input('Insira a 1° nota:'))
nota_2 = float(input('Insira a 2° nota:'))
nota_3 = float(input('Insira a 3° nota:'))
nota_4 = float(input('Insira a 4° nota:'))

media_aritmetica = (nota_1+nota_2+nota_3+nota_4)/4

print(f'A média aritmética das notas é igual à {media_aritmetica}')
