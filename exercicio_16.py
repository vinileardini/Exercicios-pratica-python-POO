#O Sr. Garcia é o proprietário de uma pequena fábrica de chocolates e precisa de sua ajuda para controlar sua produção. Ele fabrica três tipos de chocolates: ao leite, branco e amargo, cada um com um tempo diferente de produção.

#Ele gostaria de um programa que ajudasse a calcular quantas barras de cada tipo de chocolate ele pode produzir em um determinado número de horas, levando em consideração o tempo de produção para cada tipo.

#Aqui estão as informações que você precisa:

#- Chocolate ao leite: 2 horas por barra
#- Chocolate branco: 1,5 horas por barra
#- Chocolate amargo: 2,5 horas por barra

#**Requisitos:**
#1. O programa deve perguntar ao usuário o número de horas disponíveis para a produção.
#2. O programa deve perguntar ao usuário qual tipo de chocolate ele deseja produzir.
#3. O programa deve calcular e exibir o número de barras que podem ser produzidas no tempo especificado para o tipo de chocolate escolhido.
#4. O programa deve perguntar ao usuário se ele gostaria de calcular a produção para outro tipo de chocolate ou encerrar o programa.

#**Restrições:**
#- As horas disponíveis para produção devem ser um número positivo.
#- O tipo de chocolate deve ser uma das três opções disponíveis.

#**Exemplo de Execução:**
#Horas disponíveis para produção: 10
#Qual tipo de chocolate você deseja produzir? (ao leite/branco/amargo): ao leite
#Você pode produzir 5 barras de chocolate ao leite em 10 horas.
#Deseja calcular para outro tipo de chocolate? (sim/não): não (se digitar "sim", deve reiniciar o programa/loop)

#Lembre-se de testar o programa para diferentes números de horas e tipos de chocolates para garantir que ele funcione corretamente. Isso ajudará o Sr. Garcia a otimizar a produção em sua fábrica de chocolates! 


